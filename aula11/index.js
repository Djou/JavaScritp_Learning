/**
 * Artimeticos
 * + - / *
 * ** potencia 
 * % Resto da divisão
 */

//Precedencia da operação
// () ** * / % + -  

const num1 = 10;
const num2 = 5;
console.log(num1 + num2);
console.log(num1 ** num2);
console.log(num1 % num2);


let contador = 1;
contador++;
console.log(contador);

++contador;
console.log(contador);

--contador;
console.log(contador++);
console.log(++contador);

console.log(contador--);
console.log(--contador);
